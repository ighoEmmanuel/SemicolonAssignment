def get_dict(number:int):
	return { x: x ** 2 for x in range(1, number + 1) if x % 2 == 0} 

def get_dicti(numbers:list, input:list):
	return ({number: inputs  for number,inputs in zip(numbers, input)})
print(get_dicti(["tawio", "kenny"],[4, 5]))
def get_value(number):
	return[value for value in number.values()]


def get_dictis(numbers:list, input:list):
	return ({inputs: number  for number,inputs in zip( numbers, input)})
print(get_dictis(["tawio", "kenny"],[4, 5]))




	
		
